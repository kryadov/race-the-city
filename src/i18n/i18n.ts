const STORAGE_KEY = 'rtc.lang'
const listeners = new Set<(lang: Lang) => void>()

function detect(): Lang {
  try {
    const s = localStorage.getItem(STORAGE_KEY)
    if (s && (LANGS as readonly string[]).includes(s)) return s as Lang
  } catch {
    /* no localStorage (e.g. tests) */
  }
  try {
    // Match the browser language's primary subtag against what we ship — 'de-AT'
    // → 'de', 'fr-CA' → 'fr'. English is the fallback, so it needs no match.
    const nav = typeof navigator !== 'undefined' ? navigator.language?.toLowerCase() : ''
    if (nav) {
      const hit = LANGS.find((l) => nav.startsWith(l))
      if (hit) return hit
    }
  } catch {
    /* no navigator */
  }
  return 'en'
}

let current: Lang = detect()

export function getLang(): Lang {
  return current
}

export function setLang(lang: Lang): void {
  if (lang === current) return
  current = lang
  try {
    localStorage.setItem(STORAGE_KEY, lang)
  } catch {
    /* ignore persistence failures */
  }
  for (const cb of listeners) cb(lang)
}

/** Subscribe to language changes (e.g. to re-render UI text). */
export function onLangChange(cb: (lang: Lang) => void): void {
  listeners.add(cb)
}

/** Translate a key in the current language; unknown keys return the key itself. */
export function t(key: string): string {
  return MESSAGES[current][key] ?? key
}

/**
 * Every translation key defined for a language (English is the source of truth).
 * Exposed for the parity test that every language covers the same keys — no gaps
 * that would fall back to a raw key in the UI, no strays carrying dead weight.
 */
export function messageKeys(lang: Lang = 'en'): string[] {
  return Object.keys(MESSAGES[lang])
}
